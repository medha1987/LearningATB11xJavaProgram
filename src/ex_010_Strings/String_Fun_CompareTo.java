package ex_010_Strings;

public class String_Fun_CompareTo {
    public static void main(String[] args) {
        String name = "Medha";
        System.out.println(name.compareTo("Medha"));
        System.out.println(name.compareTo("dha"));
        System.out.println(name.compareTo("Med"));
        System.out.println(name.compareTo("ed"));
        System.out.println(name.compareTo("Medh"));
    }


}


